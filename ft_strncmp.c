int	ft_strncmp(char *s1, char *s2, int until)
{
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
	{
		if (until <= 0)
		{
			return (*s1 - *s2);
		}
		s1++;
		s2++;
		until--;
	}
	return (*s1 - *s2);
}
